"""Run the MegaTrain-Plus A/B benchmark across a list of models and
summarize results into a single table.

For each model, runs two configurations:
  (a) Upstream-equivalent baseline: num_buffers=2, no backward prefetch,
      no zero-copy unflatten, no store-all-activations.
  (b) MegaTrain-Plus full: defaults + store_all_activations.

Writes per-run JSONs to `docs/benchmarks/suite/<model>_{baseline,plus}.json`
and a summary markdown table to `docs/suite_summary.md`.

Usage:
    python scripts/benchmark_suite.py \
        --models "Qwen/Qwen2.5-0.5B-Instruct,Qwen/Qwen2.5-1.5B-Instruct" \
        --batch-size 2 --seq-len 512 --steps 5

Picks up any model HF hosts without trust_remote_code (public, non-gated)
by default. Safe to run on DGX Spark with unified 128 GB memory.
"""

from __future__ import annotations

import argparse
import json
import os
import subprocess
import sys
from pathlib import Path
from typing import Dict, List, Optional


def run_bench(model: str, out_path: Path, args, extra_flags: List[str]) -> Optional[dict]:
    """Invoke scripts/benchmark.py for a single model + config."""
    cmd = [
        sys.executable, "scripts/benchmark.py",
        "--model", model,
        "--batch-size", str(args.batch_size),
        "--seq-len", str(args.seq_len),
        "--steps", str(args.steps),
        "--no-optimizer",
        "--output", str(out_path),
    ] + extra_flags

    print(f"  $ {' '.join(cmd)}")
    result = subprocess.run(cmd, capture_output=True, text=True, timeout=args.timeout)
    if result.returncode != 0:
        print(f"  FAIL ({result.returncode}):")
        print("  " + result.stderr[-2000:].replace("\n", "\n  "))
        return None

    if not out_path.exists():
        print(f"  FAIL: no output JSON at {out_path}")
        return None

    with open(out_path) as f:
        return json.load(f)


def run_one_model(model: str, args) -> Dict[str, Optional[dict]]:
    """Run baseline + plus for a single model."""
    slug = model.replace("/", "_").replace("-", "_")
    out_dir = Path(args.output_dir)
    out_dir.mkdir(parents=True, exist_ok=True)
    baseline_path = out_dir / f"{slug}_baseline.json"
    plus_path = out_dir / f"{slug}_plus.json"

    print(f"\n=== {model} ===")

    print("  [baseline] no prefetch, 2 buffers, no zero-copy")
    baseline = run_bench(
        model, baseline_path, args,
        extra_flags=[
            "--num-buffers", "2",
            "--no-backward-prefetch",
            "--no-zero-copy-unflatten",
        ],
    )

    print("  [plus] defaults + store_all_activations")
    plus = run_bench(
        model, plus_path, args,
        extra_flags=["--store-all-activations"],
    )

    return {"model": model, "baseline": baseline, "plus": plus}


def fmt_delta(old: float, new: float) -> str:
    if old <= 0:
        return "-"
    pct = (new / old - 1.0) * 100
    return f"{pct:+.1f}%"


def to_summary_md(results: List[Dict], args) -> str:
    lines = [
        f"# MegaTrain-Plus Multi-Model Benchmark Suite",
        "",
        f"Generated by `scripts/benchmark_suite.py` on "
        f"**batch_size={args.batch_size}, seq_len={args.seq_len}, "
        f"steps={args.steps}** (step 0 = warmup, excluded from averages).",
        "",
        "Baseline = upstream-equivalent "
        "(`--num-buffers 2 --no-backward-prefetch --no-zero-copy-unflatten`). "
        "Plus = MegaTrain-Plus defaults + `--store-all-activations`.",
        "",
        "## Summary (step time and throughput)",
        "",
        "| Model | Params | Baseline step | Plus step | Δ step | Baseline tok/s | Plus tok/s | Δ tok/s | GPU mem Δ | Loss match |",
        "|---|---|---|---|---|---|---|---|---|---|",
    ]
    for r in results:
        m = r["model"]
        base = r["baseline"]
        plus = r["plus"]
        if base is None or plus is None:
            lines.append(f"| {m} | - | RUN FAILED | - | - | - | - | - | - | - |")
            continue

        bs = base["summary"]
        ps = plus["summary"]
        params = f"{base['num_params_B']:.2f}B"

        # Loss match: check step-by-step
        loss_match = "yes"
        for sb, sp in zip(base["steps"], plus["steps"]):
            if abs(sb["loss"] - sp["loss"]) > 0.001:
                loss_match = f"DIFF ({abs(sb['loss'] - sp['loss']):.1e})"
                break

        mem_delta = ps["peak_gpu_GB"] - bs["peak_gpu_GB"]
        lines.append(
            f"| {m} | {params} | "
            f"{bs['avg_step_time_s']:.3f}s | {ps['avg_step_time_s']:.3f}s | "
            f"**{fmt_delta(bs['avg_step_time_s'], ps['avg_step_time_s'])}** | "
            f"{bs['tokens_per_sec']:.1f} | {ps['tokens_per_sec']:.1f} | "
            f"**{fmt_delta(bs['tokens_per_sec'], ps['tokens_per_sec']):s}** | "
            f"{mem_delta:+.2f} GB | {loss_match} |"
        )

    # Detail table
    lines += [
        "",
        "## Forward / Backward Breakdown",
        "",
        "| Model | Baseline fwd | Plus fwd | Δ fwd | Baseline bwd | Plus bwd | Δ bwd | Baseline BWD/FWD | Plus BWD/FWD |",
        "|---|---|---|---|---|---|---|---|---|",
    ]
    for r in results:
        m = r["model"]
        base = r["baseline"]
        plus = r["plus"]
        if base is None or plus is None:
            continue
        bs = base["summary"]
        ps = plus["summary"]
        lines.append(
            f"| {m} | "
            f"{bs['avg_fwd_time_s']:.3f}s | {ps['avg_fwd_time_s']:.3f}s | "
            f"**{fmt_delta(bs['avg_fwd_time_s'], ps['avg_fwd_time_s']):s}** | "
            f"{bs['avg_bwd_time_s']:.3f}s | {ps['avg_bwd_time_s']:.3f}s | "
            f"**{fmt_delta(bs['avg_bwd_time_s'], ps['avg_bwd_time_s']):s}** | "
            f"{bs['bwd_to_fwd_ratio']:.2f}x | {ps['bwd_to_fwd_ratio']:.2f}x |"
        )
    lines.append("")
    return "\n".join(lines)


def main():
    parser = argparse.ArgumentParser(description="Cross-model A/B benchmark")
    parser.add_argument(
        "--models", type=str, required=True,
        help="Comma-separated HuggingFace model IDs",
    )
    parser.add_argument("--batch-size", type=int, default=2)
    parser.add_argument("--seq-len", type=int, default=512)
    parser.add_argument("--steps", type=int, default=5)
    parser.add_argument("--timeout", type=int, default=600,
                        help="Per-run timeout in seconds")
    parser.add_argument("--output-dir", type=str, default="docs/benchmarks/suite")
    parser.add_argument("--summary", type=str, default="docs/suite_summary.md")
    args = parser.parse_args()

    models = [m.strip() for m in args.models.split(",") if m.strip()]
    print(f"Benchmarking {len(models)} models: {models}")

    results = []
    for model in models:
        r = run_one_model(model, args)
        results.append(r)

    md = to_summary_md(results, args)
    Path(args.summary).write_text(md)
    print(f"\nSummary written to {args.summary}")
    print("\n" + md)


if __name__ == "__main__":
    main()
